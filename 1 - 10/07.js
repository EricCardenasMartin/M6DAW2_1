var n = 5;

console.log("N: " + n);

n += 10;

console.log("N + 10: " + n);

n -= 50;

console.log("N - 50: " + n);

n /= 3;

console.log("N / 3: " + n);

n *= 7;

console.log("N * 7: " + n);

n %= 2;

console.log("N % 2: " + n);

n++;

console.log("N++: " + n);

++n;

console.log("++N: " + n);

n--;

console.log("N--: " + n);

--n;

console.log("--N: " + n);